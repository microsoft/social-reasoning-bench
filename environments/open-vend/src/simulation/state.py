"""State management for the OpenVend simulation."""

from __future__ import annotations

import json
from dataclasses import dataclass, field
from enum import Enum
from pathlib import Path
from typing import Any


class SlotSize(str, Enum):
    SMALL = "small"
    LARGE = "large"


class OrderStatus(str, Enum):
    PENDING = "pending"  # Order placed, not yet paid
    PAID = "paid"  # Payment sent, awaiting delivery
    DELIVERED = "delivered"  # Items delivered to warehouse


@dataclass
class Email:
    id: str
    from_address: str
    to_address: str
    subject: str
    content: str
    timestamp: str
    read: bool = False

    def to_dict(self) -> dict[str, Any]:
        return {
            "id": self.id,
            "from_address": self.from_address,
            "to_address": self.to_address,
            "subject": self.subject,
            "content": self.content,
            "timestamp": self.timestamp,
            "read": self.read,
        }

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> Email:
        return cls(**data)


@dataclass
class WarehouseItem:
    product_id: str
    name: str
    quantity: int
    unit_cost: float  # What we paid per item
    size: SlotSize

    def to_dict(self) -> dict[str, Any]:
        return {
            "product_id": self.product_id,
            "name": self.name,
            "quantity": self.quantity,
            "unit_cost": self.unit_cost,
            "size": self.size.value,
        }

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> WarehouseItem:
        data["size"] = SlotSize(data["size"])
        return cls(**data)


@dataclass
class MachineSlot:
    slot_id: int
    size: SlotSize
    product_id: str | None = None
    product_name: str | None = None
    quantity: int = 0
    capacity: int = 10  # Small=10, Large=8
    price: float = 0.0

    @property
    def empty(self) -> bool:
        return self.quantity == 0

    def to_dict(self) -> dict[str, Any]:
        return {
            "slot_id": self.slot_id,
            "size": self.size.value,
            "product_id": self.product_id,
            "product_name": self.product_name,
            "quantity": self.quantity,
            "capacity": self.capacity,
            "price": self.price,
        }

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> MachineSlot:
        data["size"] = SlotSize(data["size"])
        return cls(**data)


@dataclass
class Order:
    order_id: str
    supplier_email: str
    items: list[dict[str, Any]]  # [{product_id, name, quantity, unit_cost, size}]
    total_cost: float
    status: OrderStatus
    order_day: int
    delivery_day: int  # When it will arrive

    def to_dict(self) -> dict[str, Any]:
        return {
            "order_id": self.order_id,
            "supplier_email": self.supplier_email,
            "items": self.items,
            "total_cost": self.total_cost,
            "status": self.status.value,
            "order_day": self.order_day,
            "delivery_day": self.delivery_day,
        }

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> Order:
        data["status"] = OrderStatus(data["status"])
        return cls(**data)


@dataclass
class ProductEconomics:
    """Cached economic parameters for a product (generated by LLM once)."""

    product_id: str
    reference_price: float  # Fair market price
    base_sales: float  # Average daily sales at reference price
    price_elasticity: float  # How sensitive demand is to price changes

    def to_dict(self) -> dict[str, Any]:
        return {
            "product_id": self.product_id,
            "reference_price": self.reference_price,
            "base_sales": self.base_sales,
            "price_elasticity": self.price_elasticity,
        }

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> ProductEconomics:
        return cls(**data)


