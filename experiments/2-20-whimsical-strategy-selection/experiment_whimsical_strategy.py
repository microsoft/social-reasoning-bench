"""Whimsical Strategy Selection Experiment.

Tests multiple adversarial strategies generated by WhimsyGen to find the most
effective approach for the calendar scheduling benchmark.

Before running this experiment, generate the whimsical data files:
    ./experiments/2-20-whimsical-strategy-selection/generate_data.sh

Usage:
    uv run sagebench calendar --experiments experiments/2-20-whimsical-strategy-selection/experiment_whimsical_strategy.py
"""

from pathlib import Path

from sage_benchmark.calendar_scheduling.experiments import ExperimentConfig

# Experiment directory (relative to repo root)
EXPERIMENT_DIR = Path("experiments/2-20-whimsical-strategy-selection")

# Configuration
ASSISTANT_MODEL = "trapi/gpt-4.1"
JUDGE_MODEL = "trapi/gpt-4.1"
INPUT_DATA = "data/calendar-scheduling/final/small.yaml"
NUM_STRATEGIES = 20
MAX_ROUNDS = 10
BATCH_SIZE = 21

OUTPUT_BASE = EXPERIMENT_DIR / "outputs"

# Requestor model configurations: (model, reasoning_effort)
REQUESTOR_MODELS: dict[str, tuple[str, str | None]] = {
    "gpt4.1": ("trapi/gpt-4.1", None),
    "gpt5.1-none": ("trapi/gpt-5.1", None),
    "gpt5.2-medium": ("trapi/gpt-5.2", "medium"),
    # "gemini3-flash-medium": ("gemini-3-flash-preview", "medium"),
}


def _make_config(
    paths: list[str],
    variant: str,
    requestor_model: str,
    requestor_reasoning_effort: str | None = None,
) -> ExperimentConfig:
    """Create an experiment config."""
    return ExperimentConfig(
        paths=paths,
        max_rounds=MAX_ROUNDS,
        batch_size=BATCH_SIZE,
        # Assistant configuration
        assistant_model=ASSISTANT_MODEL,
        assistant_system_prompt="privacy-ci",
        expose_preferences=True,
        assistant_explicit_cot=False,
        # Requestor configuration
        requestor_model=requestor_model,
        requestor_reasoning_effort=requestor_reasoning_effort,
        requestor_explicit_cot=False,
        # Judge configuration
        judge_model=JUDGE_MODEL,
        # Output
        output_dir=OUTPUT_BASE,
        variant=variant,
    )


def experiment_baseline():
    """Baseline: non-malicious requestor across all requestor models."""
    for req_label, (req_model, req_effort) in REQUESTOR_MODELS.items():
        yield _make_config(
            paths=[INPUT_DATA],
            variant=f"baseline_{req_label}",
            requestor_model=req_model,
            requestor_reasoning_effort=req_effort,
        )


def experiment_strategies():
    """Test each whimsical strategy variant across all requestor models."""
    for i in range(NUM_STRATEGIES):
        strategy_data = EXPERIMENT_DIR / f"data/strategy_{i}.yaml"
        for req_label, (req_model, req_effort) in REQUESTOR_MODELS.items():
            yield _make_config(
                paths=[str(strategy_data)],
                variant=f"strategy_{i}_{req_label}",
                requestor_model=req_model,
                requestor_reasoning_effort=req_effort,
            )
