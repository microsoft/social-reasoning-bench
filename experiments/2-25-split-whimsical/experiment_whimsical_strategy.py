"""Split Whimsical Strategy Selection Experiment.

Tests separate privacy and duty-of-care adversarial strategies generated by
WhimsyGen, each with its own focused attack objective.

Before running this experiment, generate the whimsical data files:
    ./experiments/2-25-split-whimsical/generate_data.sh

Usage:
    uv run sagebench calendar --experiments experiments/2-25-split-whimsical/experiment_whimsical_strategy.py
"""

from pathlib import Path

from sage_benchmark.calendar_scheduling.experiments import ExperimentConfig

# Experiment directory (relative to repo root)
EXPERIMENT_DIR = Path("experiments/2-25-split-whimsical")

# Configuration
ASSISTANT_MODEL = "gemini-2.5-flash"  # "trapi/gpt-4.1"
ASSISTANT_REASONING_EFFORT = 1024  # None
JUDGE_MODEL = "trapi/gpt-4.1"
INPUT_DATA = "data/calendar-scheduling/final/small.yaml"
NUM_STRATEGIES = 10
MAX_ROUNDS = 10
BATCH_SIZE = 21

OUTPUT_BASE = EXPERIMENT_DIR / "outputs"

# Task types with separate strategy sets
TASK_TYPES = ["privacy"]  # , "duty_of_care"]

# Requestor model configurations: (model, reasoning_effort)
REQUESTOR_MODELS: dict[str, tuple[str, str | int | None]] = {
    # "gpt4.1": ("trapi/gpt-4.1", None),
    # "gemini-3-flash": ("gemini-3-flash-preview", "none"),
    "gemini-2.5-flash-1024": ("gemini-2.5-flash", 1024)
}


def _make_config(
    paths: list[str],
    variant: str,
    requestor_model: str,
    requestor_reasoning_effort: str | int | None = None,
    expose_preferences: bool = True,
) -> ExperimentConfig:
    """Create an experiment config."""
    return ExperimentConfig(
        paths=paths,
        max_rounds=MAX_ROUNDS,
        batch_size=BATCH_SIZE,
        # Assistant configuration
        assistant_model=ASSISTANT_MODEL,
        assistant_system_prompt="privacy-ci",
        expose_preferences=expose_preferences,
        assistant_explicit_cot=False,
        # Requestor configuration
        requestor_model=requestor_model,
        requestor_reasoning_effort=requestor_reasoning_effort,
        requestor_explicit_cot=False,
        # Judge configuration
        judge_model=JUDGE_MODEL,
        # Output
        output_dir=OUTPUT_BASE,
        variant=variant,
    )


def experiment_baseline():
    """Baseline: non-malicious requestor across all requestor models."""
    for req_label, (req_model, req_effort) in REQUESTOR_MODELS.items():
        yield _make_config(
            paths=[INPUT_DATA],
            variant=f"baseline_{req_label}",
            requestor_model=req_model,
            requestor_reasoning_effort=req_effort,
        )


def experiment_strategies():
    """Test each whimsical strategy variant across task types and requestor models."""
    for task_type in TASK_TYPES:
        for i in range(NUM_STRATEGIES):
            strategy_data = EXPERIMENT_DIR / f"data/{task_type}/strategy_{i}.yaml"
            for req_label, (req_model, req_effort) in REQUESTOR_MODELS.items():
                yield _make_config(
                    paths=[str(strategy_data)],
                    variant=f"{task_type}_{i}_{req_label}",
                    requestor_model=req_model,
                    requestor_reasoning_effort=req_effort,
                    expose_preferences=task_type != "privacy",
                )
